char	*ft_strcapitalize(char *str)
{
	char	*s;

	s = str;
	if (*s != '\0')
	{
		if ((*s >= 'a') && (*s <= 'z'))
			*s = *s - 32;
		s++;
		while (*s != '\0')
		{
			if (*s >= 'A' && *s <= 'Z')
				*s = *s + 32;
			if (!((*(s - 1) >= '0' && *(s - 1) <= '9') || (*(s - 1) >= 'a'
						&& *(s - 1) <= 'z')
					|| (*(s - 1) >= 'A' && *(s - 1) <= 'Z')))
				if (*s >= 'a' && *s <= 'z')
					*s = *s - 32;
			s++;
		}
	}
	return (str);
}
