int	ft_str_is_alpha(char *str)
{
	int	result;

	result = 1;
	while (*str != '\0')
	{
		if (!((*str >= 'A') && (*str <= 'Z')))
		{
			if (!((*str >= 'a') && (*str <= 'z')))
				result = 0;
		}
		str++;
	}
	return (result);
}
