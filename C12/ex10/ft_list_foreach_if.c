#include <stdlib.h>
#include "ft_list.h"

void	ft_list_foreach_if(t_list *begin_list, void (*f)(void *),
	void *data_ref, int (*cmp)())
{
	t_list	*node;

	node = begin_list;
	while (node)
	{
		if (!(*cmp)(node->data, data_ref))
			(*f)(node->data);
		node = node->next;
	}
}
